class DocGenieAnalyzer:
    """Analyze Python functions and generate professional docstrings"""

    @staticmethod
    def extract_function_signature(code):
        """Extract function name, parameters, return type from code"""
        try:
            tree = ast.parse(code)

            # Ensure it's a function definition and not empty
            if not tree.body or not isinstance(tree.body[0], ast.FunctionDef):
                print("No function definition found or invalid code structure.")
                return None

            func_def = tree.body[0]
            func_name = func_def.name
            params = []

            # Prepare default values, mapping them to arguments
            # Defaults are for trailing arguments
            defaults_map = {}
            num_args_with_defaults = len(func_def.args.defaults)
            if num_args_with_defaults > 0:
                for i, default_node in enumerate(func_def.args.defaults):
                    arg_index = len(func_def.args.args) - num_args_with_defaults + i
                    arg_name = func_def.args.args[arg_index].arg
                    defaults_map[arg_name] = ast.unparse(default_node)

            # Extract parameters
            for arg in func_def.args.args:
                param_type = "Any"
                if arg.annotation:
                    # ast.unparse is available from Python 3.9+
                    param_type = ast.unparse(arg.annotation) if hasattr(ast, 'unparse') else "Any"

                params.append({
                    "name": arg.arg,
                    "type": param_type,
                    "default": defaults_map.get(arg.arg)
                })

            # Extract return type
            return_type = "None"
            if func_def.returns:
                return_type = ast.unparse(func_def.returns) if hasattr(ast, 'unparse') else "Any"

            return {
                "name": func_name,
                "parameters": params,
                "return_type": return_type
            }

        except SyntaxError as e:
            print(f"SyntaxError in provided code: {e}")
            return None
        except Exception as e:
            print(f"An unexpected error occurred: {e}")
            return None

